     */
    public function index()
    {
        $greet = Generator::getGreeting(date('h'));
        $menu = Menu::getMenu();
        $user = User::getProfileByUsername(session()->get("username_key"));
        
        //Set active nav
        session()->put('active_nav', 'profile');
        session()->forget('active_subnav');

        return view ('profile.index')
            ->with('menu', $menu)
            ->with('user', $user)
            ->with('greet',$greet);
    }

